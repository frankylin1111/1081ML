int main()
{
	int a[5][5];
	a[0][0]=3,a[0][1]=6,a[0][2]=2,a[0][3]=1,a[0][4]=0;
	a[1][0]=3,a[1][1]=9,a[1][2]=0,a[1][3]=8,a[1][4]=0;
	a[2][0]=2,a[2][1]=1,a[2][2]=5,a[2][3]=6,a[2][4]=0;
	a[3][0]=7,a[3][1]=2,a[3][2]=7,a[3][3]=4,a[3][4]=0;
	a[4][0]=0,a[4][1]=0,a[4][2]=0,a[4][3]=0,a[4][4]=0;
	
	for (int i=0;i<4;i++)
	{
		for (int j=0;j<4;j++)
		{
			a[i][4]+=a[i][j];  // 橫向元素之和 
			a[4][j]+=a[i][j];  // 縱向元素之和 
			a[4][4]+=a[i][j];  // 總和 
		}
	}
	
	for (int i=0;i<5;i++)
	{
		for (int j=0;j<5;j++)
		{
			printf ("%3d",a[i][j]);
		}
		printf("\n");
    }
}   
